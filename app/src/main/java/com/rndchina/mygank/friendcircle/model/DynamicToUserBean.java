public class DynamicToUserBean {
    private String user_name;
    private String user_pics;
    private String user_cover_pics;

    public String getUser_name() {
        return user_name;
    }

    public void setUser_name(String user_name) {
        this.user_name = user_name;
    }

    public String getUser_pics() {
        return user_pics;
    }

    public void setUser_pics(String user_pics) {
        this.user_pics = user_pics;
    }

    public String getUser_cover_pics() {
        return user_cover_pics;
    }

    public void setUser_cover_pics(String user_cover_pics) {
        this.user_cover_pics = user_cover_pics;
    }
}
